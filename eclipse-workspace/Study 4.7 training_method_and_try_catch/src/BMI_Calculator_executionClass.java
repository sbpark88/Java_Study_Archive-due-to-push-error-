import java.util.Scanner;

public class BMI_Calculator_executionClass {
	public static void main(String[] args) {
		BMI_Calculator bmi = new BMI_Calculator();	// BMI_Calculator라는 클래스를 bmi라 이름 붙이고 초기화 함.
		
		// 값을 입력 받음.		
		Scanner s = new Scanner(System.in);
		
		System.out.println("키(cm)를 입력해주세요.");
		double height = s.nextDouble();
		
		System.out.println("몸무게(kg)를 입력해주세요.");
		double weight = s.nextDouble();
			
		
		System.out.println(bmi.BMICal(height, weight));	// bmi클래스야 BMICal 메소드를 실행해.
	}

}
