
public class SumMachine {						// SumMachine 이라는 클래스를 만들었다.
	public void makeSum() {						// makeSum 이라는 메소드를 만들었다.
		System.out.println("make sum......");	// "make sum......"을 출력하라.
	}
	
	public static void main(String[] args) {	// main 자동완성.
		SumMachine m = new SumMachine();		// SumMachin을 m이라 이름 정하고 초기화 해.
		m.makeSum();							// m클래스에서 makeSum 메소드를 실행해.
	}

}
